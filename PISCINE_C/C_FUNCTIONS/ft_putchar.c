#include<unistd.h>

int ft_putchar(char c)
{
write(1, &c, 1);
return (0);
}

int main()
{
int i=0;
char c=97;

	while(i < 26)
	{
	ft_putchar(c);
	c++;
	i++;
	}

return (0);
}
