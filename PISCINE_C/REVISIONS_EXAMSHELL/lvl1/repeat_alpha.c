int letter_count(char c)
{
	int repeat;
	if (c >= 'A' && c <= 'Z')
		repeat = c - 'A' + 1;
	else if (c >= 'a' && c <= 'z')
		repeat = c -'a' + 1;
	return (repeat);
}


